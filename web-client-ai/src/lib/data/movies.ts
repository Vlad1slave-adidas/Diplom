export type PaceValue = 'slow' | 'balanced' | 'fast'
export type DepthValue = 'light' | 'layered' | 'complex'
export type Provider = 'start' | 'premier' | 'viju'

export interface Movie {
	id: number
	slug: string
	title: string
	year: number
	country: string
	genres: string[]
	duration: number
	ageRating: number
	rating: number
	shortDescription: string
	description: string
	poster: string
	backdrop: string
	moodLabel: string
	moodValue: MoodValue
	paceLabel: string
	paceValue: PaceValue
	depthLabel: string
	depthValue: DepthValue
	familyFriendly: boolean
	provider: Provider
}

