const backdrops = [
	'/images/devices/intro.png',
	'/images/devices/smart-tv.png',
	'/images/devices/smartphones.png',
	'/images/instructions/lg.png',
	'/images/instructions/android-tv.png',
	'/images/instructions/xiaomi.png',
]

function createMovie(
	id: number,
	data: Omit<Movie, 'id' | 'poster' | 'backdrop' | 'provider'>,
): Movie {
	const providers: Provider[] = ['start', 'premier', 'viju']

	return {
		id,
		poster,
		backdrop: backdrops[(id - 1) % backdrops.length],
		provider: providers[(id - 1) % providers.length],
		...data,
	}
}

