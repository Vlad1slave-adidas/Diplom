def esc(text: str) -> str:
    return html.escape(text, quote=False)


def clean(text: str) -> str:
    text = text.replace("\t", " ")
    text = re.sub(r" +", " ", text)
    return text.strip()


def read(path: str) -> str:
    return (ROOT / path).read_text(encoding="utf-8")


def docx_text(path: str) -> str:
    with zipfile.ZipFile(ROOT / path) as z:
        xml = z.read("word/document.xml").decode("utf-8")
    text = re.sub(r"</w:p[^>]*>", "\n", xml)
    text = re.sub(r"<[^>]+>", "", text)
    text = html.unescape(text)
    text = re.sub(r"\n{3,}", "\n\n", text)
    return text.strip()


def material(path: str, max_chars: int) -> list[str]:
    text = docx_text(path)
    paragraphs = [clean(part) for part in re.split(r"\n\s*\n", text) if clean(part)]
    if paragraphs and re.match(r"^\d+(\.\d+)*\s+", paragraphs[0]):
        paragraphs = paragraphs[1:]

    selected: list[str] = []
    total = 0
    for paragraph in paragraphs:
        if total + len(paragraph) > max_chars:
            break
        selected.append(paragraph)
        total += len(paragraph)
    return [p(paragraph) for paragraph in selected]


def p(text: str = "", style: str = "Normal", align: str | None = None, bold: bool = False,
      size: int | None = None, page_break: bool = False) -> str:
    jc = f'<w:jc w:val="{align}"/>' if align else ""
    style_tag = f'<w:pStyle w:val="{style}"/>' if style != "Normal" else ""
    page = '<w:pageBreakBefore/>' if page_break else ""
    ppr = f"<w:pPr>{style_tag}{jc}{page}</w:pPr>" if style_tag or jc or page else ""
    rpr = ""
    if bold or size:
        b = "<w:b/>" if bold else ""
        sz = f'<w:sz w:val="{size * 2}"/><w:szCs w:val="{size * 2}"/>' if size else ""
        rpr = f"<w:rPr>{b}{sz}</w:rPr>"
    return f"<w:p>{ppr}<w:r>{rpr}<w:t xml:space=\"preserve\">{esc(text)}</w:t></w:r></w:p>"


def pb() -> str:
    return '<w:p><w:r><w:br w:type="page"/></w:r></w:p>'


def heading(text: str, level: int = 1, page_break: bool = False) -> str:
    if level == 1:
        align = "left" if re.match(r"^\d+\s", text) else "center"
        return p(text, "Heading1", align=align, page_break=page_break)
    return p(text, "Heading2")


def bullet(text: str) -> str:
    return p("– " + clean(text), "Normal")


def code_block(text: str, max_lines: int | None = None) -> list[str]:
    lines = text.splitlines()
    if max_lines:
        lines = lines[:max_lines]
    result: list[str] = []
    for line in lines:
        result.append(p(line[:180], "Code", size=10))
    return result


def table(rows: list[list[str]], widths: list[int] | None = None) -> str:
    if not rows:
        return ""
    cols = len(rows[0])
    widths = widths or [9000 // cols] * cols
    grid = "".join(f'<w:gridCol w:w="{w}"/>' for w in widths)
    body = []
    for r_index, row in enumerate(rows):
        cells = []
        for c_index, cell in enumerate(row):
            shade = '<w:shd w:fill="D9EAF7"/>' if r_index == 0 else ""
            tcpr = f'<w:tcPr><w:tcW w:w="{widths[c_index]}" w:type="dxa"/>{shade}</w:tcPr>'
            cell_paras = "".join(p(part, "TableText", bold=(r_index == 0)) for part in str(cell).split("\n"))
            cells.append(f"<w:tc>{tcpr}{cell_paras}</w:tc>")
        body.append("<w:tr>" + "".join(cells) + "</w:tr>")
    return f'<w:tbl><w:tblPr><w:tblW w:w="0" w:type="auto"/><w:tblBorders><w:top w:val="single" w:sz="4"/><w:left w:val="single" w:sz="4"/><w:bottom w:val="single" w:sz="4"/><w:right w:val="single" w:sz="4"/><w:insideH w:val="single" w:sz="4"/><w:insideV w:val="single" w:sz="4"/></w:tblBorders></w:tblPr><w:tblGrid>{grid}</w:tblGrid>{"".join(body)}</w:tbl>'


def para_many(text: str) -> list[str]:
    return [p(clean(part)) for part in re.split(r"\n\s*\n", text.strip()) if clean(part)]


